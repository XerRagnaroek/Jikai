package me.xer.bot.config;

public enum ConfigOption {
	/**
	 * Whatever string or char triggers commands.
	 */
	TRIGGER,
	/**
	 * The channel the bot posts automated stuff in.
	 */
	CHANNEL,

	/**
	 * The server's timezone.
	 */
	TIMEZONE,

	/**
	 * The hash of the latest season search.
	 */
	LATEST_SEASON_HASH;
}
